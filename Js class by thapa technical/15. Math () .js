// 1. Math.abs() find the distance of a value form 0 mean 
     // if we put 5 then 0 ---- 5 distance is 5 
    // if we put -5 then -5 --- 0 distance is 5 

console.log(Math.abs(5));
console.log(Math.abs(-5));
console.log(Math.abs(-598));


// 2. Math.round() find the approximate value.
console.log(Math.round(8.9));
console.log(Math.round(9.09));
console.log(Math.round(9.699));


// 3. Math.floor()  it return the lower value in integer form 
     // mean 3.4 lower value is 3 
     // mean -3.4 lower value is -4
console.log(Math.floor(3.4));
console.log(Math.floor(99.09));
console.log(Math.floor(9.699));
console.log(Math.floor(9.699));
console.log(Math.floor(-3.4));



